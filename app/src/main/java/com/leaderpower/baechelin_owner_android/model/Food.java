package com.leaderpower.baechelin_owner_android.model;

public class Food {
    private String count, extra_options, name, price;

    public String getCount() {
        return count;
    }

    public void setCount(String count) {
        this.count = count;
    }

    public String getExtra_options() {
        return extra_options;
    }

    public void setExtra_options(String extra_options) {
        this.extra_options = extra_options;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
